# parameters and arguments

# an argument is an input to a method
# methods can have more than one inputs


def praise_person(name, age) # (name) is expecting a string/argument
  puts "#{name} is great and is #{age}"
  puts "#{name} is awesome #{age}"
  puts "#{name} is super-duper #{age}"
end

praise_person("Logan", 25) # praise person is called and the name argument is represented by Logan
praise_person("Kincaid", 32) # the name string variable is being pushed to the custom method name field
