# object methods
# a method is a command for an object

p "hello world".upcase!
p "hello world".length
p "hello world".capitalize!
p "HELLO WORLD".downcase!

foo = "hello world"
p foo.length
p foo.upcase
p foo.downcase
p foo.capitalize

# calls a method on a variable that is assigned to the string

p 10
p 10.next
# .next adds a number / goes to next number

puts "hello world".inspect

