# writing own methods

def introduce_myself
  puts "I am handsome"
  puts "I am talented"
  puts "I am brilliant"
end

# def defines a method
# anything between def and end is what the method does
# end ends the method

introduce_myself

# type the method to call / invoke the method to run it
# can be reused over and over again
# able to be invoked with or without ()
# method must be defined before being called
# cannot be called before it is created
